package com.cheyitou.common.model.po;

import java.math.BigDecimal;
import java.util.Date;

public class Order {

	private Integer id;
	private String orderNum;//订单编号
	private String orderCount;//订单数量
	private BigDecimal accountBalance;//账号余额
	private String status;//状态 1|审核 2|成功
	private Integer userId;//user表的冗余字段
	private Integer resId;//预约id
	private String frontImg;//正面
	private String backImg;//背面
	private String sideImg;//侧面
	private Date getCreate;//创建时间
    private Date getModified;//修改时间
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getOrderNum() {
		return orderNum;
	}
	public void setOrderNum(String orderNum) {
		this.orderNum = orderNum;
	}
	public String getOrderCount() {
		return orderCount;
	}
	public void setOrderCount(String orderCount) {
		this.orderCount = orderCount;
	}
	public BigDecimal getAccountBalance() {
		return accountBalance;
	}
	public void setAccountBalance(BigDecimal accountBalance) {
		this.accountBalance = accountBalance;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public Integer getUserId() {
		return userId;
	}
	public void setUserId(Integer userId) {
		this.userId = userId;
	}
	public String getFrontImg() {
		return frontImg;
	}
	public void setFrontImg(String frontImg) {
		this.frontImg = frontImg;
	}
	public String getBackImg() {
		return backImg;
	}
	public void setBackImg(String backImg) {
		this.backImg = backImg;
	}
	public String getSideImg() {
		return sideImg;
	}
	public void setSideImg(String sideImg) {
		this.sideImg = sideImg;
	}
	public Date getGetCreate() {
		return getCreate;
	}
	public void setGetCreate(Date getCreate) {
		this.getCreate = getCreate;
	}
	public Date getGetModified() {
		return getModified;
	}
	public void setGetModified(Date getModified) {
		this.getModified = getModified;
	}
	public Integer getResId() {
		return resId;
	}
	public void setResId(Integer resId) {
		this.resId = resId;
	}
	
}